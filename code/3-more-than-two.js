function moreThanTwo() {
  let num = prompt("Give me a number")

  if (num < 2){
    alert(num + "is smaller than 2")
  } if (num = 0){
    alert(num + "is the same as 2")
  } else {
    alert(num + "is larger than 2")
  }
}
